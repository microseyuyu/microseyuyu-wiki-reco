<div class="custom-container tip"><svg xmlns="http://www.w3.org/2000/svg" xmlns:xlink="http://www.w3.org/1999/xlink" viewBox="0 0 24 24"><g fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"><circle cx="12" cy="12" r="9"></circle><path d="M12 8h.01"></path><path d="M11 12h1v4h1"></path></g></svg><p class="custom-container-title">TIP</p><h4 id="_1-九品中正制与科举制" tabindex="-1"><a class="header-anchor" href="#_1-九品中正制与科举制" aria-hidden="true">#</a> 1. 九品中正制与科举制</h4>
<h4 id="_1-1-九品中正制" tabindex="-1"><a class="header-anchor" href="#_1-1-九品中正制" aria-hidden="true">#</a> 1.1 九品中正制</h4>
<ul>
<li><strong>定义</strong>：九品中正制是唐朝由曹魏所创立的选官制度，长期存在。它通过考量官员的家事、道德和才能，进行人才选拔。</li>
<li><strong>简要解释</strong>：九品中正制通过综合考察官员的家世背景、品德和能力，用九个品级来评定官员的地位和职位，高品级的官员地位较高，低品级地位较低。</li>
</ul>
<p><strong>帮助理解</strong>：这种制度在早期起到选拔人才的作用，但随着时间推移，由于过于看重家世，导致九品中正制渐渐沦为士族特权的工具。士族占据优势，人才选拔的公平性逐渐丧失。</p>
<h5 id="_1-2-科举制" tabindex="-1"><a class="header-anchor" href="#_1-2-科举制" aria-hidden="true">#</a> 1.2 科举制</h5>
<ul>
<li><strong>定义</strong>：隋朝后废除九品中正制，采取类似现代考试制度进行官员选拔。</li>
<li><strong>发展历程</strong>：隋炀帝时期形成科举制，唐朝继承并不断更新。</li>
<li><strong>唐代更新</strong>：唐太宗增加新科目，武则天扩大科举取士人数，首创武举和殿试。唐玄宗任用高官主持考试（监考员）。</li>
</ul>
<p><strong>帮助理解</strong>：科举制度是一种通过考试选拔官员的制度，相比九品中正制，它更加公平和开放。后来唐朝不断对科举制度进行改进，增加新科目和考试形式，提高了选拔官员的效率和准确性。</p>
<h3 id="_2-三省六部制" tabindex="-1"><a class="header-anchor" href="#_2-三省六部制" aria-hidden="true">#</a> 2. 三省六部制</h3>
<ul>
<li><strong>定义</strong>：隋文帝确立三省六部制，包括中书省、门下省和尚书省，以及六个部门：吏、户、礼、兵、刑、工。</li>
<li><strong>作用</strong>：三省充当了宰相的职位，提高了任用宰相的范围。政事堂设立提高了工作效率，体现了三省一体化趋势。</li>
<li><strong>问题</strong>：宰相权力过大导致腐败，部门间可能产生矛盾和冲突，唯分数论现象出现。</li>
</ul>
<p><strong>帮助理解</strong>：三省六部制是一种相对高效的政治制度，通过设立三省和六部门来分工管理国家事务。然而，宰相权力过大也带来了腐败问题，不同部门之间的矛盾可能影响政府的稳定运作。</p>
<h3 id="_3-赋税制度" tabindex="-1"><a class="header-anchor" href="#_3-赋税制度" aria-hidden="true">#</a> 3. 赋税制度</h3>
<ul>
<li><strong>魏晋时期</strong>：实行租调制，按户征收粮食和绢帛。</li>
<li><strong>北魏孝文帝改革</strong>：颁布均田令，规定一夫一妻每年纳粟为租，纳帛或布为调。</li>
<li><strong>唐初赋税</strong>：对象定位为21-59岁成年男子，除租调外，不征役的男子可纳绢或布代役（庸）。</li>
<li><strong>天宝年间两税制</strong>：每户按人丁和资产缴纳户税，按田亩缴纳地税，取消租庸调和杂税，一年分夏秋两季纳税。</li>
</ul>
