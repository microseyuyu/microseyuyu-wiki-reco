<h2 id="_3-赋税制度" tabindex="-1"><a class="header-anchor" href="#_3-赋税制度" aria-hidden="true">#</a> 3. 赋税制度</h2>
<ul>
<li><strong>魏晋时期</strong>：实行租调制，按户征收粮食和绢帛。</li>
<li><strong>北魏孝文帝改革</strong>：颁布均田令，规定一夫一妻每年纳粟为租，纳帛或布为调。</li>
<li><strong>唐初赋税</strong>：对象定位为21-59岁成年男子，除租调外，不征役的男子可纳绢或布代役（庸）。</li>
<li><strong>天宝年间两税制</strong>：每户按人丁和资产缴纳户税，按田亩缴纳地税，取消租庸调和杂税，一年分夏秋两季纳税。</li>
</ul>
